type Lookup = Record<string, true>;

function makeLookup<const T extends readonly string[]>(items: T): Lookup {
  const table = Object.create(null) as Lookup;

  for (let i = 0; i < items.length; i++) {
    table[items[i]!] = true;
  }

  return table;
}

