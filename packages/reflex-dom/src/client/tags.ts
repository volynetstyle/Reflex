export type HTMLTag = keyof typeof HTML_TAGS;
export type SVGTag = keyof typeof SVG_TAGS;
export type VoidTag = keyof typeof VOID_TAGS;

/** Ultra-fast lookups (branchless except early-exit) */

export const isHTMLTag = (tag: string): boolean =>
  HTML_TAGS[tag as HTMLTag] === true;

export const isSVGTag = (tag: string): boolean =>
  SVG_TAGS[tag as SVGTag] === true;

export const isVoidTag = (tag: string): boolean =>
  VOID_TAGS[tag as VoidTag] === true;

/**
 * Unified lookup with no switch
 */
export function hasTag(tag: string, type: "html" | "svg" | "void"): boolean {
  return type === "html"
    ? HTML_TAGS[tag as HTMLTag] === true
    : type === "svg"
      ? SVG_TAGS[tag as SVGTag] === true
      : VOID_TAGS[tag as VoidTag] === true;
}
