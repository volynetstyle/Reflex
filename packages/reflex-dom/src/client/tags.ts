/** Creates a null-prototype lookup object for maximal speed */
function makeLookup(items: readonly string[]) {
  const table: Record<string, true> = Object.create(null);

  for (let i = 0, len = items.length; i < len; i++) {
    table[items[i]!] = true;
  }

  return table;
}

